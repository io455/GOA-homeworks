a = 8
b = 3

print("a =", a)
print("b =", b)

# == ტოლია
print("a == b:", a == b)      # False

# != არ უდრის
print("a != b:", a != b)      # True

# > მეტია
print("a > b:", a > b)        # True

# < ნაკლებია
print("a < b:", a < b)        # False

# >= მეტია ან ტოლი
print("a >= b:", a >= b)      # True

# <= ნაკლებია ან ტოლი
print("a <= b:", a <= b)      # False
